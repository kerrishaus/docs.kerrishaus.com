        <h1>Simple Instructions to Pair GDO200 to the Ryobi GDO mobile app.</h1>
        <ol>
            <li>Tap the three lines in the top left of the screen to open the pull-out menu.</li>
            <li>Tap add opener.</li>
            <li>Press and hold the Wi-Fi button on the GDO until the button begins to blink.</li>
            <li>Open the Wi-Fi settings on your phone, and connect to the Ryobi GDO xxx network.</li>
            <li>If the app asks for permission to find local wireless devices, allow it.</li>
            <li>Select the Wi-Fi network that you want the GDO connected to in the list provided in the app.</li>
            <li>Enter the passcode for that network.</li>
            <li>Name your GDO.</li>
            <br/>
            <div class='alert alert-warning'>
                <i class='fa fa-warning'></i>&nbsp;The app might say that you have to pair to the Relay Hub and Door Sensor. You don&apos;t. Just close the app and reopen it, and your GDO should be listed and functional.<br/><strong>Confirmed working 26/02/2021.</strong>
            </div>
            <li>The GDO is now paired, and ready to be used.</li>
        </ol>
